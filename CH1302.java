public class CH1302 {
    public static void main(String[] args) {
        int num = 12, den = 0;

        if (den != 0) {
            int ans = num / den;
            System.out.println("Result of division: " + ans);
        } else {
            System.out.println("除數為 0");
        }

        System.out.println("End of main()!!");
    }
}
